#include <stdio.h>

/*
    静态数据
    概念: 静态数据是指一些永久性的数据, 一般存储在硬盘中
    存储的时长: 计算机关闭之后在开启, 这些数据依旧还在. 只要不主动删掉或者硬盘没坏, 这些数据永远都在
    哪些是静态数据: 静态数据一般是以文件的形式存储在硬盘上, 比如文档、照片、视频等
 */

/*
    动态数据
    概念: 动态数据是指在程序运行过程中, 动态产生的临时数据, 一般存储在内存中
    存储的时长: 计算机关闭之后, 这些临时数据就会被清除
    哪些是动态数据: 当某个程序(软件)运行时, 整个程序就被加载到内存中, 在程序运行过程中, 会产生各种各样的临时数据. 这些临时数据都是存储在内存中的. 当程序停止运行或计算机被强制关闭时, 这个程序产生的所有临时数据都会被清除
 */

/*
    静态数据和动态数据的相互转换:
        静态数据到动态数据 -- 从磁盘加载到内存
        动态数据到静态数据 -- 如手机拍照片存储到手机硬盘上
 */

/*
    数据的计量单位:
        不管是静态还是动态数据, 都是0和1组成.
        数据都有大小, 静态数据占用磁盘空间, 动态数据占用内存空间
        数据越大, 包含的0和1越多
        1 B(Byte[字节]) = 8 bit(位)
        1 KB(KByte) = 1024 B
        1 MB = 1024 KB
        1 GB = 1024 MB
        1 TB = 1024 GB
 */

