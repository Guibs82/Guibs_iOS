/*
   a)数据类型关键字:
    基本数据类型(5):
        void:   声明函数无返回值或无参数, 声明无类型指针, 显式丢弃运算结果
        char:   字符型类型数据, 属于整型数据的一种
        int:    整型数据, 通常为编译器指定的机器字长
        float:  单精度浮点型数据, 属于浮点型数据的一种
        double: 双精度浮点型数据, 属于浮点数据的一种
    类型修饰关键字(4):
        short:    修饰int, 短整型数据, 可省略被修饰的int
        long:     修饰int, 长整型数据, 可以省略被修饰的int
        signed:   修饰整型数据, 有符号数据类型
        unsigned: 修饰整型数据, 无符号数据类型
    复杂类型关键字(5):
        struct:  结构体声明
        union:   共用体声明
        enum:    枚举声明
        typedef: 声明类型别名
    存储级别关键字(6):
        auto:     指定为自动变量, 由编译器自动分配及释放. 通常在栈上分配
        static:   指定为静态变量, 分配为静态变量区, 修饰函数时, 指定函数作用域为文件内部
        register: 指定为寄存器变量, 建议编译器将变量存储到寄存器中使用, 也可以修饰函数形参, 建议编译器通过寄存器而不是堆栈传递参数
        extern:   指定对应变量为外部变量, 即标示变量或者函数的定义在别的文件中，提示编译器遇到此变量和函数时在其他模块中寻找其定义
        const:    与volatile合称“vc特性”, 指定变量不可被当前线程/进程改变（但有可能被系统或其他线程/进程改变）
        volatile: 与const合称”vc特性“, 指定变量的值有可能会被系统或其他进程/线程改变，强制编译器每次从内存中取得该变量的值
   b)流程控制关键字:
    跳转结构(4)
        return:   在函数体中，返回特定值（或者void值，即不返回值）
        continue: 结束当前循环，开始下一轮循环
        break:    跳出当前循环或switch结构
        goto:     无条件跳转语句
    分支结构(5)
        if:      条件语句后面不需要放分号
        else:    条件语句否定分支(与if连用)
        switch:  开关语句(多重分支语句)
        case:    开关语句中的分支标记
        default: 开关语句中的 ”其他" 分支, 可选
    循环结构(3)
        for: for循环结构，for(1; 2; 3) 4; 的执行顺序为1->2->4->3->2..循环, 其中2为循环条件. 在整个for循环过程中, 表达式1只计算一次, 表达式2和表达式3可能计算多次, 也可能一次也不计算. 循环体可多次执行, 也可能一次都不执行.
        do: do循环结构, do 1 while(2); 执行顺序是1->2->1..循环, 2为循环条件
        while: while循环结构, while(1) 2; 执行的顺序是1->2->1..循环, 1为循环条件
        以上循环语句, 当循环条件表达式为真则继续循环, 为假则跳出.
 */

